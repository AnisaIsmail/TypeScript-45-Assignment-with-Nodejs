// Name cases:store a person' Name in a veriale, and then print that person's name in lowercase, uppercase, and titlecase.

let Name: string = "Anisa";

// lower cases
console.log(Name.toLowerCase());

//upper cases
console.log(Name.toUpperCase());

//titlecase
console.log(Name.charAt(0).toUpperCase());

console.log(Name.slice(0.4).toLowerCase()); 




